def Align(filepath,imgDim):
    #filepath = Path('F:/DeepFaceLab/workspace/data_src/aligned/00776.jpg')
    filepath = Path(filepath)

    if filepath.suffix == '.png':
        dflimg = DFLPNG.load(str(filepath), print_on_no_embedded_data=True)
    elif filepath.suffix == '.jpg':
        dflimg = DFLJPG.load(str(filepath), print_on_no_embedded_data=True)
    else:
        print("%s is not a dfl image file" % (filepath.name))
        return

    landmark = dflimg.get_landmarks()
    npLandmarks = np.float32(landmark)
    npLandmarkIndices = np.array(INNER_EYES_AND_BOTTOM_LIP)

    rgbImg = cv2.imread(str(filepath))
    H = cv2.getAffineTransform(npLandmarks[npLandmarkIndices],
                               imgDim * MINMAX_TEMPLATE[npLandmarkIndices])
    thumbnail = cv2.warpAffine(rgbImg, H, (imgDim, imgDim))
    return thumbnail


